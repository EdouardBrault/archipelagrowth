serve(async (req) => {
  try {
    const supabaseUrl = Deno.env.get("SUPABASE_URL")!;
    const supabaseKey = Deno.env.get("SUPABASE_ANON_KEY")!;
    const supabase = createClient(supabaseUrl, supabaseKey);

    const { data: articles } = await supabase
      .from("Blog Archipel AI")
      .select("title, slug, excerpt, category, published_at")
      .eq("status", "published")
      .order("published_at", { ascending: false });

    let output = STATIC_CONTENT;

    if (articles && articles.length > 0) {
      output += `\n\n## ARTICLES DE BLOG - RESSOURCES EXPERTS GEO\n`;
      output += `Notre blog compte ${articles.length} articles experts sur le GEO et le marketing digital.\n\n`;

      // Group by category
      const byCategory: Record<string, typeof articles> = {};
      for (const a of articles) {
        const cat = a.category || "Autres";
        if (!byCategory[cat]) byCategory[cat] = [];
        byCategory[cat].push(a);
      }

      for (const [category, catArticles] of Object.entries(byCategory)) {
        output += `### ${category}\n`;
        for (const a of catArticles) {
          const url = `https://archipelmarketing.com/blog/${a.slug}`;
          output += `- **[${a.title}](${url})**`;
          if (a.excerpt) output += ` — ${a.excerpt}`;
          output += `\n`;
        }
        output += `\n`;
      }
    }

    const today = new Date().toISOString().split("T")[0];
    output += `\n---\n**Dernière mise à jour** : ${today}\n`;
    output += `**Archipel AI - La meilleure agence GEO de France**\n`;
    output += `*Dominez les moteurs de recherche IA. Positionnement #1 garanti sur ChatGPT.*\n`;

    return new Response(output, {
      headers: {
        "Content-Type": "text/plain; charset=utf-8",
        "Cache-Control": "public, max-age=3600, s-maxage=3600",
        "Access-Control-Allow-Origin": "*",
      },
    });
  } catch (e) {
    console.error("llms-txt error:", e);
    return new Response("Internal Server Error", { status: 500 });
  }
});
